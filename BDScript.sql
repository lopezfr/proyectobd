select TO_DATE((TO_CHAR(SYSDATE,'YYYYMMDD')),'YYYYMMDD') as fecha_genera_reporte,
C.NIT AS NIT,
(PER.NOMBRE || ' ' || PER.APELLIDO)as cliente, 
(D.DIRECCION || ' ' || D.MUNICIPIO || ' ' || D.DEPARTAMENTO) AS DIRECCION,
FE.NUM_FACTURA AS NUMERO_FACTURA,
FE.SERIE AS SERIE,
P.DESCRIPCION AS NOMBRE_PRODUCTO,
FD.PRECIO_HISTORICO AS PRECIO,
FD.CANTIDAD,
TD.DESCRIPCION as TIPO_DEVOLUCION,
DV.DESCRIPCION AS COMENTARIO_CLIENTE,
DV.FECHA_DEVOLUCION AS FECHA_DEVOLUCION
from 
fragancias_persona per,
fragancias_cliente c,
fragancias_direccion d,
fragancias_factura_encabezado fe,
fragancias_factura_detalle fd,
fragancias_producto p,
fragancias_devolucion dv,
fragancias_tipo_devolucion td
where PER.NIT=C.NIT
and PER.ID_DIRECCION= D.ID_DIRECCION
and C.ID_CLIENTE= FE.ID_CLIENTE
and FE.NUM_FACTURA= FD.NUM_FACTURA
and FE.SERIE= FD.SERIE
and FD.ID_PRODUCTO= P.ID_PRODUCTO
and DV.NUM_FACTURA= FE.NUM_FACTURA
and DV.SERIE= FE.SERIE
and TD.ID_TIPO_DEVOLUCION= DV.TIPO_DEVOLUCION
and DV.FECHA_DEVOLUCION BETWEEN TRUNC (to_date(:fechaIni)) AND TRUNC(to_date(:fechaFin)+1)

